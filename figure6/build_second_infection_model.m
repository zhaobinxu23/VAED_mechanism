function [p, y0, t_span] = build_second_infection_model( ...
    t_vac, y_vac, p_vac, vaccine_type, dose, alpha)

%% =========================
%  1. 复制基础参数
% =========================

p = p_vac;

p.vaccine_type = vaccine_type;
p.vaccine_dose = dose;
p.alpha = alpha;

%% =========================
%  2. 二次感染阶段恢复病毒入侵
% =========================

p.k4 = 1.0e-3;   % 二次感染阶段恢复病毒入侵常数
p.km = 1.0e6;
p.k6 = 1.0e8;
p.k7 = 0.01;
p.c_clear = 0.1;
p.Tc_generation = 5e-2;

p.v_start = 1;
p.k5 = 0.1;
p.a_vec = (0:p.N-1)' * p.da;
p.vin_vec = p.v_start .* exp(p.k5 .* p.a_vec);

%% 二次感染阶段使用完整病毒结合模型
% 注意：二次感染时不应该再按照疫苗类型限制结合，
% 因为挑战病毒是真实病毒，所有已有抗体都可以根据对应亲和性参与反应。
% non-RBD 抗体不受 alpha 影响，RBD 抗体受 alpha 影响。
p.binding_mode = 'both';

%% =========================
%  3. 初始条件
% =========================

T0 = 1e10;
I0 = zeros(p.N, 1);

% 二次感染初始病毒量
V0 = 1e1;

% 所有疫苗组均使用 t=1000 的 Tc
Tc0 = interp1(t_vac, y_vac(:, 403), 1000);

x0 = zeros(3604, 1);

%% ----------------------------------------------------------
%  non-RBD 抗体相关项：1:1800
%  所有接种组均取 t=1000
% ----------------------------------------------------------

for i = 1:1800
    x0(i) = interp1(t_vac, y_vac(:, i + 403), 1000);
end

%% ----------------------------------------------------------
%  RBD 抗体相关项：1801:3600
%
%  alpha = 0: 无突变，10^0 = 1，完全继承 t=1000 状态
%  alpha > 0: 按照 10^(-alpha) 保留原反应性，
%             其余部分回到 t=0 状态
% ----------------------------------------------------------

for i = 1801:3600
    x_t1000 = interp1(t_vac, y_vac(:, i + 403), 1000);
    x_t0    = interp1(t_vac, y_vac(:, i + 403), 0);

    x0(i) = 10^(-alpha) * x_t1000 + ...
            (1 - 10^(-alpha)) * x_t0;
end

%% ----------------------------------------------------------
%  其他 x0 变量
% ----------------------------------------------------------

x0(3601) = interp1(t_vac, y_vac(:, 3601 + 403), 0);

% 二次感染时，这里根据原始代码通常设置为病毒初始抗原量。
% 你原始代码片段中有：
% x0(3602) = 1e1; % vaccine or virus
x0(3602) = 1e1;

x0(3603) = 0;
x0(3604) = 0;

cum_death_0 = [0; 0; 0];
L_age_0 = zeros(p.N, 1);

y0 = [T0; ...
      I0; ...
      V0; ...
      Tc0; ...
      x0; ...
      cum_death_0; ...
      L_age_0];

%% =========================
%  4. 模拟时间
% =========================

t_span = [0, 1000];

end